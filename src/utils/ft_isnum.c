int	ft_is_num(char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[0] == '+' || s[0] == '-')
			i++;
		if (!(s[i] >= '0' && s[i] <= '9'))
			return (1);
		i++;
	}
	return (0);
}